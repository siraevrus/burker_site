/**
 * Добавляет страницы (privacy, и т.д.) в БД, если их ещё нет.
 * Не затрагивает товары и другие данные.
 */
import "dotenv/config";
import { PrismaClient } from "@prisma/client";

const prisma = new PrismaClient();

const PRIVACY_CONTENT = `
<h2>1. Общие положения</h2>
<p>Настоящая Политика конфиденциальности определяет порядок обработки и защиты персональных данных пользователей интернет-магазина Мира Брендс | Буркер (далее — «Сайт»).</p>
<p>Использование Сайта означает безоговорочное согласие пользователя с настоящей Политикой и указанными в ней условиями обработки его персональной информации.</p>

<h2>2. Собираемая информация</h2>
<p>При использовании Сайта мы можем собирать следующую информацию:</p>
<ul>
<li>Имя и контактные данные (телефон, email, адрес доставки)</li>
<li>Информация о заказах и покупках</li>
<li>Технические данные (IP-адрес, тип браузера, операционная система)</li>
<li>Данные о взаимодействии с Сайтом</li>
</ul>

<h2>3. Цели использования информации</h2>
<p>Собранная информация используется для:</p>
<ul>
<li>Обработки и выполнения заказов</li>
<li>Связи с клиентами по вопросам заказов</li>
<li>Улучшения качества обслуживания</li>
<li>Отправки информационных сообщений (с согласия пользователя)</li>
<li>Обеспечения безопасности и предотвращения мошенничества</li>
</ul>

<h2>4. Защита персональных данных</h2>
<p>Мы применяем современные методы защиты информации и обеспечиваем конфиденциальность персональных данных в соответствии с требованиями законодательства РФ.</p>

<h2>5. Передача данных третьим лицам</h2>
<p>Мы не передаем персональные данные третьим лицам, за исключением случаев, когда это необходимо для выполнения заказа (например, службам доставки) или требуется по закону.</p>

<h2>6. Права пользователей</h2>
<p>Пользователь имеет право:</p>
<ul>
<li>Получать информацию о своих персональных данных</li>
<li>Требовать исправления неточных данных</li>
<li>Требовать удаления персональных данных</li>
<li>Отозвать согласие на обработку персональных данных</li>
</ul>

<h2>7. Cookies</h2>
<p>Сайт использует файлы cookie для улучшения работы и персонализации опыта пользователя. Вы можете настроить браузер для отказа от cookies, однако это может ограничить функциональность Сайта.</p>

<h2>8. Контакты</h2>
<p>По вопросам, связанным с обработкой персональных данных, вы можете обратиться к нам через форму обратной связи на Сайте или по контактным данным, указанным в разделе «Контакты».</p>

<h2>9. Изменения в Политике</h2>
<p>Мы оставляем за собой право вносить изменения в настоящую Политику конфиденциальности. Актуальная версия всегда доступна на данной странице.</p>
<p><strong>Дата последнего обновления:</strong> ${new Date().toLocaleDateString("ru-RU", { year: "numeric", month: "long", day: "numeric" })}</p>
`;

async function main() {
  const existing = await prisma.page.findUnique({ where: { slug: "privacy" } });
  if (existing) {
    console.log("Страница /privacy уже существует, пропускаем.");
  } else {
    await prisma.page.create({
      data: {
        title: "Политика конфиденциальности",
        slug: "privacy",
        content: PRIVACY_CONTENT.trim(),
        category: "policies",
        published: true,
        seoTitle: "Политика конфиденциальности",
        seoDescription: "Политика конфиденциальности Мира Брендс | Буркер",
      },
    });
    console.log("Страница /privacy создана.");
  }
  console.log("Готово.");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
